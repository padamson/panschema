    fn schema_with_class(class: ClassDefinition) -> SchemaDefinition {
        let mut schema = SchemaDefinition::new("s");
        schema.classes.insert(class.name.clone(), class);
        schema
    }

    /// Parses `sql` through `pg_query` — a binding to Postgres's own C
    /// parser (`libpg_query`) — and panics with the real parse error if
    /// it isn't valid Postgres syntax. A real-Postgres-grammar oracle,
    /// not a second copy of this module's own assumptions. Every test
    /// that asserts on rendered DDL calls this too.
    fn assert_valid_postgres_sql(sql: &str) {
        if let Err(e) = pg_query::parse(sql) {
            panic!("generated DDL is not valid Postgres SQL: {e}\n\nSQL:\n{sql}");
        }
    }

