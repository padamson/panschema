        let mut schema = SchemaDefinition::new("s");
        let mut status_enum = EnumDefinition::new("DeploymentStatus");
        status_enum
            .permissible_values
            .insert("actual".to_string(), PermissibleValue::new("actual"));
        status_enum
            .permissible_values
            .insert("planned".to_string(), PermissibleValue::new("planned"));
