//! Postgres DDL writer
//!
//! Writes the LinkML IR to Postgres `CREATE TABLE` DDL: concrete classes,
//! scalar attributes, enums, and single-valued class references. See
//! `docs/features/24-postgres-ddl-writer.md` for the full design,
//! including constructs not yet covered here.

use std::collections::BTreeMap;
use std::fmt::Write as _;
use std::path::Path;

use crate::io::{IoError, IoResult, Writer};
use crate::linkml::{ClassDefinition, SchemaDefinition};

/// Writer for Postgres DDL (`CREATE TABLE` / `CREATE TYPE`).
pub struct PostgresWriter;

impl PostgresWriter {
    pub fn new() -> Self {
        Self
    }

    /// Render the schema to a complete DDL script.
    pub fn render(&self, schema: &SchemaDefinition) -> String {
        render(schema)
    }
}

impl Default for PostgresWriter {
    fn default() -> Self {
        Self::new()
    }
}

impl Writer for PostgresWriter {
    fn write(&self, schema: &SchemaDefinition, output: &Path) -> IoResult<()> {
        if needs_parent_dir(output) {
            std::fs::create_dir_all(output.parent().unwrap()).map_err(IoError::Io)?;
        }
        std::fs::write(output, self.render(schema)).map_err(IoError::Io)?;
        Ok(())
    }

    fn format_id(&self) -> &str {
        "postgres"
    }
}

/// Whether `output` names a parent directory that might need creating.
/// False for a bare filename with no directory component (`.parent()`
/// there is `Some("")`, not `None`, so the check can't just be
/// `.is_some()`).
fn needs_parent_dir(output: &Path) -> bool {
    output.parent().is_some_and(|p| !p.as_os_str().is_empty())
}

fn render(schema: &SchemaDefinition) -> String {
    let mut out = String::new();
    writeln!(
        out,
        "-- @generated by panschema v{}",
        env!("CARGO_PKG_VERSION")
    )
    .ok();
    writeln!(out, "-- Schema: {}", schema.name).ok();
    writeln!(out).ok();

    for enum_name in referenced_enums(schema) {
        let def = &schema.enums[enum_name];
        let type_name = crate::rust_writer::snake_case(enum_name);
        let values: Vec<String> = def
            .permissible_values
            .keys()
            .map(|v| format!("'{}'", v.replace('\'', "''")))
            .collect();
        writeln!(
            out,
            "CREATE TYPE {type_name} AS ENUM ({});",
            values.join(", ")
        )
        .ok();
    }
    writeln!(out).ok();

    // Foreign-key constraints are deferred: added via `ALTER TABLE` after
    // every `CREATE TABLE`, so declaration order (and cycles between
    // classes) never matter — the script still applies in one pass.
    let mut fk_constraints = Vec::new();
    let skips = compute_skips(schema);

    for (class_name, class) in &schema.classes {
        if class.r#abstract {
            // Not meant to be instantiated — nothing to hold a row.
            continue;
        }
        if skips.contains_key(class_name) {
            // Not yet supported (is_a / multivalued / any_of / references
            // a skipped class) — see `skipped_classes` for the diagnostic
            // surfaced to the caller.
            continue;
        }
        let table = crate::rust_writer::snake_case(class_name);
        let effective = crate::linkml_resolve::resolve_effective_slots(class, schema);
        let (pk_col, pk_type) = class_primary_key(class, schema);

        // The identifier-marked slot (if any) becomes the primary key
        // column instead of a synthetic one.
        let pk_slot = effective
            .iter()
            .find(|(_, slot)| slot.identifier)
            .map(|(name, _)| name.clone());

        writeln!(out, "CREATE TABLE {table} (").ok();
        let mut lines = vec![format!(
            "    {pk_col} {pk_type} PRIMARY KEY{}",
            if pk_slot.is_none() {
                " DEFAULT gen_random_uuid()"
            } else {
                ""
            }
        )];
        for (slot_name, slot) in &effective {
            if pk_slot.as_deref() == Some(slot_name.as_str()) {
                continue;
            }
            let not_null = if slot.required { " NOT NULL" } else { "" };
            let range = slot.range.as_deref();
            if let Some(target_class) = range.and_then(|r| schema.classes.get_key_value(r)) {
                let (target_name, target_def) = target_class;
                let (target_pk_col, target_pk_type) = class_primary_key(target_def, schema);
                // Named after the target's actual primary key column
                // (matching LinkML's own relmodel_transformer convention)
                // rather than a hardcoded `_id` suffix, which wouldn't
                // even refer to a real column when the target's key is
                // named something else (e.g. an `identifier` slot `code`).
                let col = format!(
                    "{}_{}",
                    crate::rust_writer::snake_case(slot_name),
                    target_pk_col
                );
                lines.push(format!("    {col} {target_pk_type}{not_null}"));
                fk_constraints.push(FkConstraint {
                    from_table: table.clone(),
                    from_col: col,
                    to_table: crate::rust_writer::snake_case(target_name),
                    to_col: target_pk_col,
                    constraint_name: format!(
                        "{table}_{}_fkey",
                        crate::rust_writer::snake_case(slot_name)
                    ),
                });
            } else {
                let col = crate::rust_writer::snake_case(slot_name);
                let sql_type = sql_type_for_slot_range(range, schema);
                lines.push(format!("    {col} {sql_type}{not_null}"));
            }
        }
        writeln!(out, "{}", lines.join(",\n")).ok();
        writeln!(out, ");").ok();
        writeln!(out).ok();
    }

    for fk in &fk_constraints {
        writeln!(
            out,
            "ALTER TABLE {} ADD CONSTRAINT {} FOREIGN KEY ({}) REFERENCES {} ({});",
            fk.from_table, fk.constraint_name, fk.from_col, fk.to_table, fk.to_col
        )
        .ok();
    }

    out
}

/// A deferred foreign-key constraint, added after every table is declared.
struct FkConstraint {
    from_table: String,
    from_col: String,
    to_table: String,
    to_col: String,
    constraint_name: String,
}

/// A class this writer doesn't project to a table, and why.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct SkippedClass {
    pub class: String,
    pub reason: String,
}

/// Classes [`render`] skips, with a diagnostic naming why — `is_a`,
/// a multivalued slot, an `any_of` slot, or a class-range slot that
/// targets one of those (which would otherwise emit a foreign key to a
/// table that's never created), are all not yet supported. An abstract
/// class isn't reported here — it's not a gap, it deliberately gets no
/// table.
pub fn skipped_classes(schema: &SchemaDefinition) -> Vec<SkippedClass> {
    compute_skips(schema)
        .into_iter()
        .map(|(class, reason)| SkippedClass { class, reason })
        .collect()
}

/// The shared computation behind [`skipped_classes`] and [`render`]'s own
/// exclusion of out-of-scope classes — one pass so the two can't drift.
fn compute_skips(schema: &SchemaDefinition) -> BTreeMap<String, String> {
    let mut skips = BTreeMap::new();
    for (name, class) in &schema.classes {
        if class.r#abstract {
            continue;
        }
        if class.is_a.is_some() {
            skips.insert(
                name.clone(),
                "uses `is_a`, which this writer does not yet support".to_string(),
            );
            continue;
        }
        let effective = crate::linkml_resolve::resolve_effective_slots(class, schema);
        if let Some((slot_name, _)) = effective.iter().find(|(_, s)| s.multivalued) {
            skips.insert(
                name.clone(),
                format!(
                    "has multivalued slot `{slot_name}`, which this writer does not yet support"
                ),
            );
            continue;
        }
        if let Some((slot_name, _)) = effective.iter().find(|(_, s)| !s.any_of.is_empty()) {
            skips.insert(
                name.clone(),
                format!(
                    "has polymorphic `any_of` slot `{slot_name}`, which this writer does not yet support"
                ),
            );
        }
    }

    // Second pass: a class-range slot targeting an abstract or
    // already-skipped class would emit a foreign key to a table that's
    // never created — skip the referencing class too, rather than emit
    // broken DDL. Single pass (not a fixed point): a chain of skips more
    // than one hop deep is an edge case this writer doesn't chase.
    let is_includable = |name: &str| {
        schema.classes.get(name).is_some_and(|c| !c.r#abstract) && !skips.contains_key(name)
    };
    let mut cascaded = Vec::new();
    for (name, class) in &schema.classes {
        if class.r#abstract || skips.contains_key(name) {
            continue;
        }
        let effective = crate::linkml_resolve::resolve_effective_slots(class, schema);
        for (slot_name, slot) in &effective {
            if let Some(range) = &slot.range
                && schema.classes.contains_key(range)
                && !is_includable(range)
            {
                cascaded.push((
                    name.clone(),
                    format!(
                        "references class `{range}` (via slot `{slot_name}`), which is unsupported or itself skipped"
                    ),
                ));
                break;
            }
        }
    }
    skips.extend(cascaded);

    skips
}

/// The primary-key column name and SQL type for a class: its effective
/// `identifier` slot if one exists, else the synthesized `id uuid` key
/// every table falls back to.
fn class_primary_key(class: &ClassDefinition, schema: &SchemaDefinition) -> (String, String) {
    let effective = crate::linkml_resolve::resolve_effective_slots(class, schema);
    match effective.iter().find(|(_, slot)| slot.identifier) {
        Some((name, slot)) => {
            let col = crate::rust_writer::snake_case(name);
            let sql_type = sql_type_for_slot_range(slot.range.as_deref(), schema);
            (col, sql_type)
        }
        None => ("id".to_string(), "uuid".to_string()),
    }
}

/// Every enum referenced by an effective slot of any class, in name-sorted
/// (deterministic) order — the set of `CREATE TYPE`s to emit before any
/// table that references one.
fn referenced_enums(schema: &SchemaDefinition) -> Vec<&str> {
    let mut names = std::collections::BTreeSet::new();
    for class in schema.classes.values() {
        let effective = crate::linkml_resolve::resolve_effective_slots(class, schema);
        for slot in effective.values() {
            if let Some(range) = &slot.range
                && let Some((enum_name, _)) = schema.enums.get_key_value(range)
            {
                names.insert(enum_name.as_str());
            }
        }
    }
    names.into_iter().collect()
}

/// The Postgres column type for a slot's range: an enum type name if the
/// range resolves to a declared enum, else the scalar type mapping.
fn sql_type_for_slot_range(range: Option<&str>, schema: &SchemaDefinition) -> String {
    let range = range.unwrap_or("string");
    if schema.enums.contains_key(range) {
        crate::rust_writer::snake_case(range)
    } else {
        sql_type_for_range(range).to_string()
    }
}

/// Map a LinkML scalar range to its Postgres column type. Mirrors
/// `rust_writer::type_for_range`'s alias set for the primitive ranges.
///
/// The first arm is unreachable-but-documented: it maps to the exact
/// same `"text"` the `_` fallback below produces, so no test can ever
/// distinguish "matched explicitly" from "fell through to default" for
/// these names (a mutation-testing run confirmed this — deleting the arm
/// is a true equivalent mutant). It stays written out anyway, as a record
/// of which names are deliberately text, not merely unrecognized.
fn sql_type_for_range(range: &str) -> &'static str {
    match range {
        "string" | "str" | "uri" | "uriorcurie" | "curie" | "ncname" | "objectidentifier"
        | "nodeidentifier" => "text",
        "integer" | "int" => "integer",
        "boolean" | "bool" => "boolean",
        "float" | "double" | "decimal" => "double precision",
        "date" => "date",
        "datetime" => "timestamptz",
        "time" => "time",
        _ => "text",
    }
}

