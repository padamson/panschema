    #[test]
    fn abstract_class_referencing_a_skipped_class_is_not_reported() {
        // Abstract classes are deliberately excluded from `skipped_classes`
        // (they get no table on purpose, which isn't a gap) — that must
        // hold even when the abstract class's own slot references another
        // class that IS a real gap (here, `Broken` uses `is_a`).
        let mut broken = ClassDefinition::new("Broken");
        broken.is_a = Some("Base".to_string());

        let mut abstract_class = ClassDefinition::new("AbstractThing");
        abstract_class.r#abstract = true;
        let mut broken_ref = SlotDefinition::new("broken_ref");
        broken_ref.range = Some("Broken".to_string());
        abstract_class
            .attributes
            .insert("broken_ref".to_string(), broken_ref);

        let mut schema = SchemaDefinition::new("s");
        schema.classes.insert("Broken".to_string(), broken);
        schema
            .classes
            .insert("AbstractThing".to_string(), abstract_class);

        let skipped = skipped_classes(&schema);
        assert!(
            !skipped.iter().any(|s| s.class == "AbstractThing"),
            "an abstract class must never appear in skipped_classes, even if its own slot references a real gap; got: {skipped:?}"
        );
        assert!(
            skipped.iter().any(|s| s.class == "Broken"),
            "Broken must still be reported for its own is_a usage; got: {skipped:?}"
        );
    }

    #[test]
    fn whole_script_with_enum_fk_and_both_pk_kinds_parses_as_one_unit() {
        // A representative multi-class fixture combining every construct
        // this writer supports together — an enum, a synthesized uuid PK,
        // an identifier-derived PK, and an FK between them — parsed as one
        // whole script (not per-statement), so a statement-ordering
        // mistake (e.g. a CREATE TYPE after the table that needs it)
        // would fail this even if each statement parsed individually.
        use crate::linkml::{EnumDefinition, PermissibleValue};
        let mut schema = SchemaDefinition::new("nimbus");

        let mut status_enum = EnumDefinition::new("DeploymentStatus");
        status_enum
            .permissible_values
            .insert("actual".to_string(), PermissibleValue::new("actual"));
        status_enum
            .permissible_values
            .insert("planned".to_string(), PermissibleValue::new("planned"));
        schema
            .enums
            .insert("DeploymentStatus".to_string(), status_enum);

        // Provider: identifier-derived (non-synthetic) primary key.
        let mut provider = ClassDefinition::new("Provider");
        let mut code = SlotDefinition::new("code");
        code.range = Some("string".to_string());
        code.identifier = true;
        provider.attributes.insert("code".to_string(), code);
        schema.classes.insert("Provider".to_string(), provider);

        // Deployment: synthesized uuid primary key, an enum column, and
        // an FK to Provider's identifier-derived key.
        let mut deployment = ClassDefinition::new("Deployment");
        let mut status = SlotDefinition::new("status");
        status.range = Some("DeploymentStatus".to_string());
        status.required = true;
        deployment.attributes.insert("status".to_string(), status);
        let mut on_provider = SlotDefinition::new("on_provider");
        on_provider.range = Some("Provider".to_string());
        deployment
            .attributes
            .insert("on_provider".to_string(), on_provider);
        schema.classes.insert("Deployment".to_string(), deployment);

        let out = PostgresWriter::new().render(&schema);
        assert_valid_postgres_sql(&out);
        // Sanity that this fixture actually exercises what it claims to
        // (a failure here means the test stopped covering what its name
        // says, independent of whether pg_query accepts the output).
        assert!(out.contains("CREATE TYPE deployment_status AS ENUM"));
        assert!(out.contains("code text PRIMARY KEY"));
        assert!(out.contains("id uuid PRIMARY KEY DEFAULT gen_random_uuid()"));
        assert!(out.contains("ALTER TABLE deployment ADD CONSTRAINT"));
    }
